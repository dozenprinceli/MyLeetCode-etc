class MyCircularDeque {

    private static class LinkedNode {
        int val; // -1è¡¨ç¤ºè¡¨å¤´node
        LinkedNode prev;
        LinkedNode next;

        public LinkedNode(int val) {
            this.val = val;
        }
    }

    private final LinkedNode head;
    private int curSize;
    private final int maxSize;

    public MyCircularDeque(int k) {
        head = new LinkedNode(-1);
        head.next = head;
        head.prev = head;
        curSize = 0;
        maxSize = k;
    }

    public boolean insertFront(int value) {
        if (!isFull()) {
            LinkedNode oldNext = head.next;
            LinkedNode newNext = new LinkedNode(value);
            head.next = newNext;
            newNext.next = oldNext;
            newNext.prev = head;
            oldNext.prev = newNext;
            curSize++;
            return true;
        }
        return false;
    }

    public boolean insertLast(int value) {
        if (!isFull()) {
            LinkedNode oldPrev = head.prev;
            LinkedNode newPrev = new LinkedNode(value);
            head.prev = newPrev;
            newPrev.prev = oldPrev;
            oldPrev.next = newPrev;
            newPrev.next = head;
            curSize++;
            return true;
        }
        return false;
    }

    public boolean deleteFront() {
        if (!isEmpty()) {
            head.next.next.prev = head;
            head.next = head.next.next;
            curSize--;
            return true;
        }
        return false;
    }

    public boolean deleteLast() {
        if (!isEmpty()) {
            head.prev.prev.next = head;
            head.prev = head.prev.prev;
            curSize--;
            return true;
        }
        return false;
    }

    public int getFront() {
        if (!isEmpty()) {
            return head.next.val;
        }
        return -1;
    }

    public int getRear() {
        if (!isEmpty()) {
            return head.prev.val;
        }
        return -1;
    }

    public boolean isEmpty() {
        return head.next == head;
    }

    public boolean isFull() {
        return curSize == maxSize;
    }
}
